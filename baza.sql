CREATE TABLE IF NOT EXISTS doctor(
    id int,
    name text,
    surname text,
    date_of_birth text,
    umbg text,
    address text,
    email text,
    phone_number text,
    salary real,
    username text,
    password text
);

CREATE TABLE IF NOT EXISTS patient(
    id int,
    name text,
    surname text,
    date_of_birth text,
    umbg text,
    address text,
    email text,
    phone_number text
);



